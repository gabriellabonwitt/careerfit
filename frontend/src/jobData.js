// ── Experience level → seniority mapping ─────────────────────────────────────
const EXPERIENCE_MAP = {
  'entry':  ['Intern', 'Entry-level'],
  '1-3':    ['Entry-level', 'Mid-level'],
  '4plus':  ['Mid-level', 'Senior'],
}

// ── Filter jobs ──────────────────────────────────────────────────────────────
export function filterJobs({ roleTitles, industries, locations, jobType, remoteOnly, experienceLevel }) {
  let results = [...JOBS]

  if (remoteOnly) results = results.filter(j => j.remote)

  if (jobType && jobType !== 'any') {
    results = results.filter(j => j.type.toLowerCase() === jobType.toLowerCase())
  }

  if (experienceLevel && EXPERIENCE_MAP[experienceLevel]) {
    const allowed = EXPERIENCE_MAP[experienceLevel]
    results = results.filter(j => allowed.includes(j.seniority))
  }

  if (roleTitles?.length) {
    const kw = roleTitles.map(t => t.toLowerCase())
    results = results.filter(j =>
      kw.some(k =>
        j.title.toLowerCase().includes(k) ||
        j.category?.toLowerCase().includes(k) ||
        j.description.toLowerCase().includes(k) ||
        j.industry.toLowerCase().includes(k)
      )
    )
  }

  if (industries?.length) {
    const ind = industries.map(i => i.toLowerCase())
    results = results.filter(j => ind.some(i => j.industry.toLowerCase().includes(i)))
  }

  if (locations?.length) {
    const loc = locations.map(l => l.toLowerCase())
    results = results.filter(j =>
      loc.some(l => j.location.toLowerCase().includes(l)) || j.remote
    )
  }

  return results.length ? results : JOBS
}
